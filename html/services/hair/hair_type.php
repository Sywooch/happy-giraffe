    <h1>Какие типы волос бывают? Определить тип волос</h1>
    <p>Постоянно ухудшающаяся экологическая обстановка, неправильное питание, нарушение обмена веществ и ряд других заболеваний влияют на рост и состояние наших волос. А ведь не только женщины, но и мужчины хотят иметь блестящие и здоровые волосы.</p>
    <p>Поэтому всегда вызывают тревогу и озабоченность негативные изменения, которые происходят с ними. Кому приятно, если волосы начинают выпадать или изменяется их структура, если они начинают выглядеть совершенно по-другому, становятся тусклыми и безжизненными?</p>
    <p>Структура волоса может меняться в зависимости и от питания. Чрезмерное употребление сладких, соленых и жирных продуктов отрицательно сказывается не только на волосах, но и на нашей коже.</p>

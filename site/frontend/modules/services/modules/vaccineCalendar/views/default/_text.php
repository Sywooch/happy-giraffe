    <p>Национальный календарь прививок был создан ведущими педиатрами и эпидемиологами страны, опираясь на весьма значимые факторы.</p>
    <p><b><em>Во-первых</em></b>, он позволяет защитить детей наиболее уязвимого возраста. Например, коклюш &ndash; смертельно опасен для детей первого года, поэтому вакцинацию начинают уже в 4,5 месяца.</p>

    <p><b><em>В-третьих</em></b>, учтено, что ко всем инфекциям иммунитет вырабатывается по-своему и сохраняется в течение разного промежутка времени. Например, прививки детям от кори делают однократно, а от дифтерии &ndash; периодически повторяют.</p>

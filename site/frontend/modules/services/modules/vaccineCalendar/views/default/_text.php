    <h3>Как прививка защищает ребенка?</h3>

    <p>Во время вакцинации ребенку вводится микроскопическое количество живого или инактивированного возбудителя конкретной инфекции (антигены). Когда антигены попадают в кровь, у ребенка начинается выработка антител к этой инфекции, то есть вырабатывается иммунитет. Заболевания не происходит, может отмечаться лишь легкое недомогание.</p>
    <p>А когда ребенок встретится &laquo;по-настоящему&raquo; с этой инфекцией, организм распознает ее как знакомую и быстро справится с ней при помощи уже &laquo;обученных&raquo; специальных клеток &ndash; антител. В результате заболевание не разовьется или возникнет в легкой форме, но уж точно не даст грозных осложнений, а подчас &ndash; смертельных.</p>

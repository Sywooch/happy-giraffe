<p align="center"><strong>Где найти картинки для блога: простые и не очень пути</strong></p>



<p>Тексты &ndash; это хорошо, но не наглядно. Картинки тоже хорошо, но не понятно. Значит, в идеале для постов в блоге необходимо использовать как текстовый контент, так и графический. Если со статьями все более-менее понятно: мы пишем их сами по интересующей нас теме, то где взять хорошие картинки для своего блога, знают далеко не все. Что ж, тут возможны варианты.</p>



<p>1. <strong>Взять фотоаппарат и сделать фотографию для блога самостоятельно</strong>.</p>



<p>Этот способ самый надежный и правильный, но, к сожалению, не всегда применимый. Вы можете не разбираться в фотографии и не знать, как сделать хороший снимок. Или предмет, фотография которого вам необходима, уже давно стал историей, и запечатлеть его нет возможности. Или тематика вашего блога такова, что делать фото самостоятельно крайне затруднительно. Но, в любом случае, на этот способ лучше всего ориентироваться и по возможности максимально им пользоваться.</p>



<p>Для обработки фотографий рекомендую бесплатную, понятную и функциональную программу Picasa от Google: <a href="http://picasa.google.com/">http://picasa.google.com/</a></p>



<p>2. <strong>Нарисовать картинку самостоятельно в графическом редакторе</strong>.</p>



<p>Этот вариант подходит для тех, кто умеет рисовать. Остальным, увы, придется искать другие пути. Или учиться изобразительному искусству, что, в общем-то, тоже неплохо.</p>



<p>3. <strong>Сделать скрин-шот сайта</strong>.</p>



<p>Если вы пишите инструкцию по регистрации, руководство по покупкам в иностранных Интернет-магазинах или прочие полезные для массового читателя мануалы, то этот способ иллюстрирования сказанного будет для вас наиболее удобен.</p>



<p>Для легкого и быстрого изготовления скрин-шотов отдельных частей экрана рекомендую бесплатную программу Gyazo: <a href="http://gyazo.com/">http://gyazo.com/</a></p>



<p>4. <strong>Сделать скрин-шот видео</strong>.</p>



<p>Видеохостингов в Сети огромное количество, но самым популярным исторически является Youtube. Он хранит множество разнообразных роликов на самые невероятные темы, которые вполне могут стать источником шикарных иллюстраций для нашего блога.</p>



<p>Скриншоты можно делать различными способами. Расскажу о самом простом, который собственноручно проверила на работоспособность и результативность.</p>



<p><em>Шаг первый</em>. Открываем сайт Youtube.com и вводим в строку поиска тему, по которой нам необходимо найти видеоролик. При формулировании поискового запроса на английском языке шансы на успех значительно возрастают. Поскольку нам для иллюстраций нужно видео хорошего качества, чтобы на скриншоте не было &laquo;квадратиков&raquo;, можно в конце поискового запроса приписать &laquo;hd&raquo;, что означает high definition или видео высокой четкости.</p>



<p><img border="0" height="568" src="/images/commentators/1-1.png" width="623" /></p>



<p><em>Шаг два</em>. Открываем понравившийся нам клип.</p>

<p><img border="0" height="382" src="/images/commentators/1-2.png" width="624" /></p>



<p><em>Шаг три</em>. Выбираем максимальное качество ролика.</p>



<p><img border="0" height="378" src="/images/commentators/1-3.png" width="624" /></p>



<p><em>Шаг четыре</em>. Разворачиваем видеоролик на полный экран.</p>

<p><img border="0" height="380" src="/images/commentators/1-4.png" width="624" /></p>



<p><em>Шаг пятый</em>. В нужном месте видеоролика нажимаем на клавиатуре кнопку &laquo;Print screen&raquo; и вставляем получившуюся картинку в любой графический редактор для сохранения. Уникальная картинка для нашего блога готова!</p>



<p><img border="0" height="351" src="/images/commentators/1-5.png" width="624" /></p>



<p>5. <strong>Поискать картинку в фотобанках</strong>.</p>



<p>Фотобанков на просторах Интернета тоже немало, и у каждого из них есть свои особенности, касающиеся легального использования размещенного на них контента. Их необходимо учитывать при заимствовании понравившихся фотографий. Лучше лишний раз перестраховаться, чем впоследствии иметь проблемы с авторами и правообладателями.</p>



<p>Рассмотрим несколько фотобанков, где можно подобрать необходимые картинки.</p>



<p><strong>Flickr.com (</strong><a href="http://flickr.com/">http://Flickr.com</a><strong>)</strong></p>



<p>Один из самых популярных фоторесурсов, чьи фонды исчисляются миллионами единиц хранения. Этот ресурс очень популярен у пользователей разных стран благодаря удобному поиску изображений. Чтобы ограничить свой выбор только теми фотографиями, которые вы, с разрешения авторов, можете использовать в своем блоге (не преследуя коммерческих целей), нужно воспользоваться функцией расширенного поиска (Advanced Search) и установить галочку в поле &laquo;Only search within Creative Commons-licensed content&raquo;. Или воспользоваться каталогом: <a href="http://www.flickr.com/creativecommons/">http://www.flickr.com/creativecommons/</a></p>



<p><img border="0" height="203" src="/images/commentators/1-6.png" width="623" /></p>



<p><strong>Stock.XCHNG (</strong><a href="http://www.sxc.hu/">http://www.sxc.hu</a><strong>)</strong></p>



<p>Этот сайт предоставляет возможность скачать большое количество фотографий самых различных тематик. Но для начала необходимо зарегистрироваться. Детали использования фотографии всегда можно уточнить в информационном поле под изображением и справа в блоке &laquo;Photo details&raquo;. Поисковый запрос необходимо формулировать на английском языке.</p>



<p><img border="0" height="565" src="/images/commentators/1-7.png" width="623" /></p>



<p><strong>Stockvault</strong> (<a href="http://www.stockvault.net/">http://www.stockvault.net/</a>)</p>



<p>Огромное количество фотографий на самые разные темы. Для пользования сайтом регистрация не требуется.</p>



<p><img border="0" height="454" src="/images/commentators/1-8.png" width="623" /></p>










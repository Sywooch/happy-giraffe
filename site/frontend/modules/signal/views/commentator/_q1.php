<p>6. <strong>Поискать картинку в </strong><strong>Google</strong>.</p>



<p>У Google очень удобный поиск по картинкам. В меню &laquo;Расширенный поиск&raquo; можно задать параметр &laquo;С лицензией на использование в некоммерческих целях&raquo;. И тогда вы сможете быть уверены, что использование понравившейся вам картинки в собственном блоге не нарушит ничьи права и в дальнейшем не принесет вам неприятностей. Главный недостаток поиска картинок через Google &ndash; их низкая уникальность в большинстве случаев.</p>



<p>7. <strong>Поискать картинку в Яндекс</strong>.</p>



<p>В поисковой системе Яндекс отсутствует настройка поиска только легального контента, однако, на каждой странице выводится политкорректная фраза с напоминанием, что у каждого изображения есть свой автор и об этом необходимо помнить. Недостаток найденных через Яндекс фотографий такой же, как и найденных в Google &ndash; низкая уникальность.</p>



<p>Итак, картинка найдена. Теперь необходимо проверить ее на уникальность. Для этого используется сервис Tineye (<a href="http://www.tineye.com/">http://www.tineye.com</a>). Он позволяет проверять уникальность картинок как находящихся на жестком диске компьютера, так и уже размещенных в Интернете (для этого необходимо указать адрес картинки). Если картинка уникальна, ее можно смело использовать. Если не уникальна, использовать ее, в принципе, тоже можно, но лучше сначала сделать уникальной.</p>



<p>Чтобы из неуникальной картинки сделать уникальную, на сегодняшний день существует три способа:</p>



<p>1. <strong>Отзеркалить изображение по горизонтальной или вертикальной оси</strong>.</p>



<p>Для этого потребуется любой графический редактор. Но такой способ не подойдет для картинок с надписями или изображений, на которых запечатлены люди с часами на руках, обручальными кольцами на пальцах и прочими деталями, несоответствие которых общепринятым нормам сразу бросается в глаза. И даже если вы зеркалите обычный пейзаж, полученный результат необходимо проверить на уникальность. Вдруг кто-то до вас догадался проделать такой же фокус с этой фотографией.</p>



<p>2. <strong>Повернуть изображение в какую-либо сторону более чем на 15 градусов.</strong></p>



<p>Для непрофессионала это достаточно трудная задача. В Сети много мастер-классов на эту тему, но без опыта подобной работы добиться удовлетворительного результата не так просто.</p>



<p>3. <strong>Сжать изображение по горизонтали или вертикали более чем на 20%.</strong></p>



<p>Как нетрудно догадаться, подобный метод уникализации фотографий может привести к серьезным искажениям на подопытном изображении. Поэтому пользоваться им рекомендуется только людям, находящимся на короткой ноге с Фотошопом. Иначе результат вас может жестоко разочаровать.</p>



<p>Как видим, поиск картинок для своего блога &ndash; дело не простое и требующее понимания некоторых тонкостей. Поэтому, как уже говорилось вначале, идеальный вариант &ndash; это собственные фотографии. Они гарантированно уникальны, и их использование регламентируется только вами.</p>
